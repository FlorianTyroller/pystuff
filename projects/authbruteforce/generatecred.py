def gen_cred(ul, pl):
    credlist = []
    for u in ul:
        for p in pl:
            credlist.append([u,p])
            credlist.append([u.capitalize(),p])
            credlist.append([u,p.capitalize()])
            credlist.append([u.capitalize(),p.capitalize()])
    return credlist


def save_credentials_to_file(credentials, filename):
    with open(filename, 'w') as file:
        for username, password in credentials:
            file.write(f"{username}:{password}\n")


